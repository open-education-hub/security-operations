#!/usr/bin/env python3
"""
Zeek Log Analyser — Quick-Start Analysis Script
=================================================
This script replicates in Python the most common zeek-cut + awk
analysis commands from Guide 01 (Intermediate).

Run against the provided sample logs in support/logs/ or
against logs generated by your own Zeek capture.

Usage:
    python3 analyse-zeek-logs.py [logs_directory]
    python3 analyse-zeek-logs.py support/logs
    python3 analyse-zeek-logs.py /var/log/zeek/current
"""

import os
import sys
from collections import Counter, defaultdict


# ──────────────────────────────────────────────────────
# Generic TSV Zeek log reader
# ──────────────────────────────────────────────────────

def read_zeek_log(filepath):
    """
    Read a Zeek TSV log file and return a list of dicts.
    Handles the #fields / #types header lines.
    """
    records = []
    fields = []
    with open(filepath, errors="replace") as f:
        for line in f:
            line = line.rstrip("\n")
            if line.startswith("#fields"):
                fields = line.split("\t")[1:]
            elif line.startswith("#") or not line:
                continue
            elif fields:
                values = line.split("\t")
                records.append(dict(zip(fields, values)))
    return records


def load_log(log_dir, name):
    """Load a named Zeek log from a directory. Returns [] if not found."""
    path = os.path.join(log_dir, name)
    if os.path.exists(path):
        return read_zeek_log(path)
    return []


# ──────────────────────────────────────────────────────
# conn.log analysis
# ──────────────────────────────────────────────────────

def analyse_conn(records):
    print("\n" + "=" * 60)
    print("CONN.LOG ANALYSIS")
    print("=" * 60)

    print(f"\nTotal connections: {len(records)}")

    # Top destination ports
    port_counts = Counter(r.get("id.resp_p", "-") for r in records)
    print("\nTop 10 destination ports:")
    for port, count in port_counts.most_common(10):
        print(f"  Port {port:>6}: {count:>5} connections")

    # Top source IPs by connection count
    src_counts = Counter(r.get("id.orig_h", "-") for r in records)
    print("\nTop 10 source IPs by connection count:")
    for ip, count in src_counts.most_common(10):
        print(f"  {ip:>18}: {count:>5} connections")

    # Failed connections (not SF or S1)
    failed = [r for r in records
              if r.get("conn_state", "") not in ("SF", "S1", "S2", "S3", "RSTO", "RSTR")]
    print(f"\nFailed/incomplete connections: {len(failed)}")
    state_counts = Counter(r.get("conn_state", "-") for r in failed)
    for state, count in state_counts.most_common():
        print(f"  {state:>6}: {count}")

    # Large outbound transfers
    large = []
    for r in records:
        try:
            ob = int(r.get("orig_bytes", "0") or "0")
            if ob > 10_000_000:  # > 10 MB
                large.append(r)
        except ValueError:
            pass
    if large:
        print(f"\nLarge outbound transfers (> 10 MB):")
        for r in sorted(large, key=lambda x: int(x.get("orig_bytes", "0") or "0"), reverse=True):
            mb = int(r.get("orig_bytes", "0") or "0") / 1_048_576
            print(f"  {r.get('id.orig_h'):>18} → {r.get('id.resp_h'):>18}:{r.get('id.resp_p')}  "
                  f"{mb:.1f} MB  state={r.get('conn_state')}")

    # Port 4444 connections (Metasploit default)
    c2_ports = [r for r in records
                if r.get("id.resp_p") in ("4444", "4445", "1337", "31337")]
    if c2_ports:
        print(f"\nWARNING: Connections to known C2 ports:")
        for r in c2_ports:
            print(f"  {r.get('id.orig_h'):>18} → {r.get('id.resp_h'):>18}:{r.get('id.resp_p')}  "
                  f"state={r.get('conn_state')}")


# ──────────────────────────────────────────────────────
# dns.log analysis
# ──────────────────────────────────────────────────────

def analyse_dns(records):
    print("\n" + "=" * 60)
    print("DNS.LOG ANALYSIS")
    print("=" * 60)

    print(f"\nTotal DNS transactions: {len(records)}")

    # Top queried domains
    domain_counts = Counter(r.get("query", "-") for r in records)
    print("\nTop 15 queried domains:")
    for domain, count in domain_counts.most_common(15):
        print(f"  {count:>5}× {domain}")

    # NXDOMAIN responses
    nxdomain = [r for r in records if r.get("rcode_name") == "NXDOMAIN"]
    print(f"\nNXDOMAIN responses: {len(nxdomain)}")
    if nxdomain:
        print("  Recent NXDOMAIN queries:")
        for r in nxdomain[:10]:
            print(f"    {r.get('id.orig_h'):>18} queried: {r.get('query')}")

    # Long subdomain labels (DGA indicator)
    suspicious_dns = []
    for r in records:
        query = r.get("query", "")
        if not query:
            continue
        parts = query.split(".")
        if parts and len(parts[0]) >= 20:
            suspicious_dns.append(r)
    if suspicious_dns:
        print(f"\nSuspicious long subdomains (possible DGA/tunnelling):")
        for r in suspicious_dns:
            print(f"  {r.get('id.orig_h'):>18} → {r.get('query')} "
                  f"(label len={len(r.get('query','').split('.')[0])})")

    # TXT record queries (tunnelling indicator)
    txt_queries = [r for r in records if r.get("qtype_name") == "TXT"]
    if txt_queries:
        print(f"\nTXT record queries ({len(txt_queries)} total — potential tunnelling):")
        for r in txt_queries[:5]:
            print(f"  {r.get('id.orig_h'):>18} queried: {r.get('query')}")
            if r.get("answers") and r.get("answers") != "-":
                print(f"    Response: {r.get('answers')}")


# ──────────────────────────────────────────────────────
# http.log analysis
# ──────────────────────────────────────────────────────

def analyse_http(records):
    print("\n" + "=" * 60)
    print("HTTP.LOG ANALYSIS")
    print("=" * 60)

    print(f"\nTotal HTTP transactions: {len(records)}")

    # Unusual user agents
    ua_counts = Counter(r.get("user_agent", "-") for r in records)
    print("\nUser agents seen:")
    for ua, count in ua_counts.most_common():
        flag = ""
        ua_lower = ua.lower()
        if any(tool in ua_lower for tool in ["sqlmap", "nmap", "nikto", "curl",
                                               "wget", "python", "go-http",
                                               "ruby", "perl", "masscan"]):
            flag = "  ← SUSPICIOUS TOOL"
        print(f"  {count:>4}× {ua}{flag}")

    # POST requests
    posts = [r for r in records if r.get("method") == "POST"]
    if posts:
        print(f"\nPOST requests ({len(posts)} total):")
        for r in posts:
            size = int(r.get("request_body_len", "0") or "0")
            print(f"  {r.get('id.orig_h'):>18} → {r.get('host')}{r.get('uri')}  "
                  f"size={size}  status={r.get('status_code')}")

    # Credentials in HTTP (Zeek extracts these from HTTP Basic Auth and form posts)
    for r in records:
        if r.get("username") and r.get("username") not in ("-", "(empty)"):
            print(f"\nCLEARTEXT CREDENTIALS captured:")
            print(f"  Source: {r.get('id.orig_h')}")
            print(f"  Host: {r.get('host')}{r.get('uri')}")
            print(f"  Username: {r.get('username')}  Password: {r.get('password')}")

    # Suspicious URI patterns
    suspicious_uris = []
    for r in records:
        uri = r.get("uri", "")
        if any(pattern in uri.lower() for pattern in
               ["../", "%2e%2e", "etc/passwd", "union", "select", "<script",
                "eval(", "base64_decode", "/admin/", ".bak", ".zip"]):
            suspicious_uris.append(r)
    if suspicious_uris:
        print(f"\nSuspicious URIs detected:")
        for r in suspicious_uris:
            print(f"  {r.get('id.orig_h'):>18} → {r.get('host')}{r.get('uri')}  "
                  f"status={r.get('status_code')}")


# ──────────────────────────────────────────────────────
# files.log analysis
# ──────────────────────────────────────────────────────

def analyse_files(records):
    print("\n" + "=" * 60)
    print("FILES.LOG ANALYSIS")
    print("=" * 60)

    print(f"\nTotal file transfers: {len(records)}")

    for r in records:
        mime = r.get("mime_type", "-")
        fname = r.get("filename", "-")
        size = int(r.get("total_bytes", "0") or "0")
        sha256 = r.get("sha256", "-")
        flag = ""
        if any(t in mime for t in ["executable", "x-pe", "x-dosexec", "x-powershell",
                                    "x-msdos", "x-msdownload", "x-sh"]):
            flag = "  ← EXECUTABLE FILE"
        elif any(t in mime for t in ["7z", "x-rar", "zip", "gzip"]):
            flag = "  ← ARCHIVE FILE"
        print(f"  {fname}  ({mime})  {size/1048576:.1f} MB  SHA256={sha256[:16]}...{flag}")


# ──────────────────────────────────────────────────────
# Main
# ──────────────────────────────────────────────────────

if __name__ == "__main__":
    log_dir = sys.argv[1] if len(sys.argv) > 1 else "logs"

    if not os.path.isdir(log_dir):
        print(f"Error: directory not found: {log_dir}")
        print(f"Usage: python3 {sys.argv[0]} <logs_directory>")
        sys.exit(1)

    print(f"Analysing Zeek logs in: {os.path.abspath(log_dir)}")

    conn_records  = load_log(log_dir, "conn.log")
    dns_records   = load_log(log_dir, "dns.log")
    http_records  = load_log(log_dir, "http.log")
    files_records = load_log(log_dir, "files.log")

    if conn_records:
        analyse_conn(conn_records)
    if dns_records:
        analyse_dns(dns_records)
    if http_records:
        analyse_http(http_records)
    if files_records:
        analyse_files(files_records)

    print("\n" + "=" * 60)
    print("Analysis complete.")
